def sub(s, r, f, **kwargs):
    def g(x):
        if isString(f):
            return f
        if x.groups():
            return f(*x.groups())
        else:
            return f(x.group())
    return re.sub(r, g, s, **kwargs)

def memoize(fn):
  store = {}
  def wrapper(*args):
    if args in store:
      return store[args]
    else:
      value = fn(*args)
      store[args] = value
      return value
  return wrapper

def dollarPrompt(x):
    if isArray(x):
        item = choose(x, mode=1)
    elif isObject(x):
        item = choose(list(x.values()), mode=1)
    else:
        item = x

    @memoize
    def f(x):
        return prompt(item=item, fallback=x) or x

    @memoize
    def g(x):
        return prompt(item, 'input:')

    while True:
        if test('\$([a-zA-Z]\w*)', item):
            item = sub(item, r, f, count=1)
        else:
            break
        
    n = 0
    while True:
        n += 1
        r = '\$' + str(n)
        if test(r, item):
            item = sub(item, r, g)
        else:
            break

    return item


