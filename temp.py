def reWrap(dict, template = ''):
    ref = {
        '': '(?:$1)',
        'b': "\\b(?:$1)\\b",
    }
    template = ref.get(template, template)
    keys = list(dict.keys() if isObject(dict) else dict)
    symbols = map(keys, re.escape)
    s = "|".join(symbols)
    return re.sub('\$1', s, template)

